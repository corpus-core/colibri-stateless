void print_fp2(const char* label, const bn254_fp2_t* val) {
    printf("%s: ", label);
    for(int i=0; i<32; i++) printf("%02x", val->c0.bytes[i]);
    printf(", ");
    for(int i=0; i<32; i++) printf("%02x", val->c1.bytes[i]);
    printf("\n");
}

